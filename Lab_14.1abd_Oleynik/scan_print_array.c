#include <stdio.h>
#include <stdlib.h>
#include <locale.h>
#include "scan_print_array.h"

/* Функция осуществляет ввод целочисленного массива m размера size_m.
 * Функция возвращает 1, если введённое число не соответствует типу данных массива, массив не передан.
 * Функция возвращает 0, если элементы массива были корректно введены.
 */
int scan_array (size_t size_m, int *m)
{
    if (!m)
        return 1;

    for (int *i = m; i < m + size_m; i ++)
        if (scanf("%d", i) != 1)
            return 1;

    return 0;
}

/* Функция осуществляет вывод целочисленного массива m размера size_m.
 * Функция возвращает 1, если массив не был передан.
 * Функция возвращает 0, если элементы массива были корректно выведены.
 */
int print_array (size_t size_m, const int *m)
{
    if (!m)
        return 1;

    for (const int *i = m; i < m + size_m; i ++)
        printf("%d\n", *i);

    return 0;
}

/* Функция осуществляет ввод вещественного массива a размера size_a.
 * Функция возвращает 1, если введённое число не соответствует типу данных массива, массив не передан.
 * Функция возвращает 0, если элементы массива были корректно введены.
 */
int fscan_array (size_t size_a, double *a)
{
    if (!a)
        return 1;

    for (double *i = a; i < a + size_a; i ++)
        if (scanf("%lf", i) != 1)
            return 1;

    return 0;
}

/* Функция осуществляет вывод вещественного массива a размера size_a.
 * Функция возвращает 1, если массив не был передан.
 * Функция возвращает 0, если элементы массива были корректно выведены.
 */
int fprint_array (size_t size_a, const double *a)
{
    if (!a)
        return 1;

    for (const double *i = a; i < a + size_a; i ++)
        printf("%f\n", *i);

    return 0;
}
